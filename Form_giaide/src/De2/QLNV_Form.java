    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            File f = new File("nhanvien.dat");
            FileWriter fw = new FileWriter(f);
            for (int i = 0; i < dsnv.size(); i++) {
                fw.write(dsnv.get(i).toString());
            }
            fw.close();
            JOptionPane.showMessageDialog(rootPane, "Lưu file thành công!");
        } catch (Exception e) {
            System.out.println("Loi lưu file: "+e);
        }
        
    }//GEN-LAST:event_btnSaveActionPerformed
    
    public boolean is_emty(){
        if(txtDC.getText().isEmpty()||txtHT.getText().isEmpty()||txtHSL.getText().isEmpty()||txtLCB.getText().isEmpty()||txtLTL.getText().isEmpty()||txtNS.getText().isEmpty()||txtTN.getText().isEmpty()){
            if (!rdNam.isSelected()&&!rdNu.isSelected()) {
                JOptionPane.showMessageDialog(rootPane, "Nhap day du thong tin!");
                return true;
            }
        }
        return false;
    }
     
