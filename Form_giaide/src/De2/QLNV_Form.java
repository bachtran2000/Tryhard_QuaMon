    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        Nhanvien nv = new Nhanvien();
        if(!is_emty()){
            nv.setDC(txtDC.getText().trim());
            nv.setGT(rdNam.getText().trim());
            nv.setHSL(Float.parseFloat(txtHSL.getText().trim()));
            nv.setHT(txtHT.getText().trim());
            nv.setLCB(Float.parseFloat(txtLCB.getText().trim()));
            nv.setNS(txtNS.getText().trim());
            nv.setPB((String) cbPB.getSelectedItem());
            nv.setTN(Float.parseFloat(txtTN.getText().trim()));
            nv.setLTL(nv.Tinh_LTL());
            txtLTL.setText(String.valueOf(nv.getLTL()));
            dsnv.add(nv);
            for(int i=0;i<dsnv.size();i++){
                System.out.println(dsnv.get(i).toString());
            }
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

