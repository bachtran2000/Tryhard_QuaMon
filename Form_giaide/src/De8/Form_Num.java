    public boolean check(float min, float max, float value) {
        if (min < max) {
            if (value > min && value < max) {
                return true;
            } else {
                JOptionPane.showMessageDialog(rootPane, "Nhập lại các giá trị phù hợp!");
                return false;
            }
        } else {

            return false;
        }
    }

    public boolean is_Prime(int a) {
        if (a <= 2) {
            return true;
        } else {
            for (int j = 2; j < a; j++) {
                if (a % j == 0) {
                    return false;
                }
            }
        }
        return true;
    }

