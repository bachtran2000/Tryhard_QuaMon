    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        nb.setMin(Float.parseFloat(txtMin.getText()));
        nb.setMax(Float.parseFloat(txtMax.getText()));
        nb.setValue(Float.parseFloat(txtValue.getText()));
        if (check(Float.parseFloat(txtMin.getText()), Float.parseFloat(txtMax.getText()), Float.parseFloat(txtValue.getText()))) {
            JOptionPane.showMessageDialog(rootPane, "Số đúng điều kiện!");
            nb.show();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Nhập lại các giá trị phù hợp!");
        }
    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (txtMax.getText().isEmpty() || txtMin.getText().isEmpty() || txtValue.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Nhập đầy đủ thông tin");
        } else {
            if (is_Prime(Integer.parseInt(txtMin.getText()))
                    && is_Prime(Integer.parseInt(txtMax.getText()))
                    && is_Prime(Integer.parseInt(txtValue.getText()))
                    && check(Float.parseFloat(txtMin.getText()),
                            Float.parseFloat(txtMax.getText()),
                            Float.parseFloat(txtValue.getText()))) {
                try {
                    File f = new File("numb.txt");
                    FileWriter fw = new FileWriter(f,true);
                    nb.setMax(Float.parseFloat(txtMax.getText()));
                    nb.setMin(Float.parseFloat(txtMin.getText()));
                    nb.setValue(Float.parseFloat(txtValue.getText()));
                    
                    fw.append(nb.toString());
                    fw.close();
                    JOptionPane.showMessageDialog(rootPane, "Lưu các số thành công!");
                } catch (Exception e) {
                    System.out.println("Loi ghi file: " + e);
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Nhập các số là số nguyên tố và đúng điều kiện!");
                txtMax.setText("");
                txtMin.setText("");
                txtValue.setText("");
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

