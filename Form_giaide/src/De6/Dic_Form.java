    private void btnTransActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTransActionPerformed
        // TODO add your handling code here:
        if (txtFind.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Nhập từ cần tìm!");
            return;
        } else if (Binary_search(txtFind.getText())==-1) {
            JOptionPane.showMessageDialog(rootPane, "Không tìm thấy từ!");
            return;
        }else {
            Word w = (Word) wd.get(Binary_search(txtFind.getText()));
            txtMean.setText(w.getVn());
        }
            
    }//GEN-LAST:event_btnTransActionPerformed

