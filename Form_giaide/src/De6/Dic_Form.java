    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        
        if (txtMean.getText().isEmpty()||txtFind.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Nhập đầy đủ thông tin!");
        } else {
            for (int i = 0; i < wd.size(); i++) {
                if (txtFind.equals(wd.get(i).getEn())) {
                    JOptionPane.showMessageDialog(rootPane, "Từ đã có trong từ điển!");
                    return;
                }
            }
            Word w = new Word(txtFind.getText(), txtMean.getText(), wd.size()+1);
            wd.add(w);
            JOptionPane.showMessageDialog(rootPane, "Thêm thành công!");
            sort();
            showList();
            txtFind.setText("");
            txtMean.setText("");
        }
    }//GEN-LAST:event_btnAddActionPerformed

